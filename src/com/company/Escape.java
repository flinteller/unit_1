package com.company;

public class Escape {
    public static void main(String[] args) {
        System.out.println("What is the difference between");
        System.out.println("a ' and a \"? Or between a \" and a \\\"? ");
        System.out.println("");
        System.out.println("One is what we see when we're typing our program. \nThe other is what appears on the \"console.\"");

        System.out.println("  \\/ \n \\\\// \n\\\\\\///\n///\\\\\\ \n //\\\\");
        System.out.print("  /\\");

    }
}
